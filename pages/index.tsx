import type { NextPage } from 'next'
import Head from 'next/head'
import Image from 'next/image'
import styles from '../styles/Home.module.css'
import Link from 'next/link';

import Movie from '../pages/movie'
import Menu from "../pages/menu";
import {useEffect, useState} from "react";

const API_Key = "af70d4de-1ee0-4425-a428-b726e4cbbe0e";
const IMAGE_API = "https://kinopoiskapiunofficial.tech/api/v2.2/films/{id}/images?type=POSTER"
const API_URL_POPULAR = "https://kinopoiskapiunofficial.tech/api/v2.2/films/top?type=TOP_100_POPULAR_FILMS&page=2";
const API_URL_SEARCH =
    "https://kinopoiskapiunofficial.tech/api/v2.1/films/search-by-keyword?keyword=";


const Home: NextPage = () => {
    const [ movies, setMovies ] = useState([]);

    useEffect( () => {
        fetch(API_URL_POPULAR, {
            headers: {
                "Content-Type": "application/json",
                "X-API-KEY": API_Key,
            },
        })
            .then(res => res.json())
            .then((data) => {
                console.log(data);
                setMovies(data.films);
            });


    }, []);
  return (
    <div className={styles.container}>
      <Head>
        <title>InnoPoisk</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="../public/favicon.ico" />
      </Head>

    <Menu/>

        <div className={styles.movie_container}>
            {movies.length > 0 && movies.map((movie) => <Movie key={movie.filmId} {...movie} />)}
    </div>
    </div>
  )
}

export default Home
